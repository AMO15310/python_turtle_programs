import turtle
t = turtle.Turtle()
#select background color
turtle.bgcolor("blue")
#select color of the pen
turtle.pencolor("pink")
#select the fill color of the pen
turtle.fillcolor("black")
#select the title of the screen
turtle.title("create a rectangle without using a loop")
#select the speed of the pen
turtle.speed(1)
#move the pe forward by 100
t.fd(100)
#turn the pen by 90 degrees
t.rt(90)
t.fd(100)
t.rt(90)
t.fd(100)
t.rt(90)
t.fd(100)
#end the loop
turtle.mainloop()
